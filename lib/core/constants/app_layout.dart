/// Shared layout tokens (spacing + radii) so common values live in one place
/// instead of being re-declared per feature constants file.
abstract final class AppSpacing {
  /// Standard screen horizontal padding.
  static const double screenHorizontal = 24.0;
}

abstract final class AppRadii {
  /// Search bar / search field corner radius.
  static const double searchBar = 14.0;
}
